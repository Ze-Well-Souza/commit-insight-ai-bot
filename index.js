// POST /api/analyze - Disparar nova an√°lise
app.post("/api/analyze", async (req, res) => {
  try {
    const { repositoryUrl, commitSha, commitMessage, author, diff } = req.body;

    // Valida√ß√µes b√°sicas
    if (!repositoryUrl || !commitMessage || !author) {
      return res.status(400).json({
        success: false,
        message: "Campos obrigat√≥rios: repositoryUrl, commitMessage, author"
      });
    }

    // Resposta imediata para o frontend
    res.status(202).json({
      success: true,
      message: "An√°lise iniciada com sucesso",
      status: "processing"
    });

    // Processar an√°lise de forma ass√≠ncrona
    (async () => {
      try {
        console.log(`üîç Iniciando an√°lise via API para: ${repositoryUrl}`);
        
        const analysisData = {
          repo: repositoryUrl,
          author: author,
          message: commitMessage,
          diff: diff || "An√°lise disparada via API - diff n√£o fornecido"
        };

        const analise = await analisarCommit(analysisData);
        
        // Salvar no banco de dados
        await saveAnalysis({
          commit_sha: commitSha || `api-${Date.now()}`,
          commit_message: commitMessage,
          author: author,
          repository: repositoryUrl,
          timestamp: new Date().toISOString(),
          analysis_content: analise,
          status: 'Completed',
          commit_url: `${repositoryUrl}/commit/${commitSha || 'unknown'}`
        });

        console.log(`‚úÖ An√°lise via API conclu√≠da e salva para: ${repositoryUrl}`);

        // Enviar notifica√ß√£o Discord (opcional)
        if (process.env.DISCORD_WEBHOOK_URL) {
          await enviarNotificacaoDiscord({
            repo: repositoryUrl,
            author: author,
            message: commitMessage,
            url: `${repositoryUrl}/commit/${commitSha || 'unknown'}`,
            analise: analise,
          });
        }

      } catch (error) {
        console.error(`‚ùå Erro na an√°lise via API: ${error.message}`);
        
        // Salvar erro no banco
        try {
          await saveAnalysis({
            commit_sha: commitSha || `api-error-${Date.now()}`,
            commit_message: commitMessage,
            author: author,
            repository: repositoryUrl,
            timestamp: new Date().toISOString(),
            analysis_content: `Erro na an√°lise: ${error.message}`,
            status: 'Failed',
            commit_url: `${repositoryUrl}/commit/${commitSha || 'unknown'}`
          });
        } catch (saveError) {
          console.error(`‚ùå Erro ao salvar an√°lise com falha: ${saveError.message}`);
        }
      }
    })();

  } catch (error) {
    console.error("‚ùå Erro ao processar requisi√ß√£o de an√°lise:", error);
    res.status(500).json({
      success: false,
      message: "Erro interno do servidor",
      error: error.message
    });
  }
});

// Webhook do GitHub (atualizado para salvar no banco)
app.post("/webhook", async (req, res) => {
  try {
    const eventType = req.headers["x-github-event"];
    console.log(`üìå Evento recebido: ${eventType || "push"}`);

    if (eventType === "ping") {
      console.log("üîî Ping recebido do GitHub");
      return res.status(200).send("‚úÖ Pong do webhook");
    }

    const { repository, author, commit_message, commit_sha, diff, commit_url } = req.body;

    if (!diff) {
      console.error("‚ùå O 'diff' do commit n√£o foi encontrado no corpo da requisi√ß√£o.");
      return res.status(400).send("O 'diff' do commit √© obrigat√≥rio.");
    }
    
    console.log(`üì¶ Recebido commit de ${repository}`);
    
    res.status(202).send("‚úÖ Webhook recebido, an√°lise em andamento.");

    // Processar an√°lise de forma ass√≠ncrona
    (async () => {
      try {
        console.log(`üîç Analisando commit: ${commit_message.substring(0, 50)}...`);
        
        const analise = await analisarCommit({
          repo: repository,
          author: author,
          message: commit_message,
          diff: diff,
        });

        // Salvar no banco de dados
        await saveAnalysis({
          commit_sha: commit_sha,
          commit_message: commit_message,
          author: author,
          repository: repository,
          timestamp: new Date().toISOString(),
          analysis_content: analise,
          status: 'Completed',
          commit_url: commit_url
        });

        console.log(`‚úÖ An√°lise do commit ${commit_sha.substring(0,7)} conclu√≠da e salva.`);

        await enviarNotificacaoDiscord({
          repo: repository,
          author: author,
          message: commit_message,
          url: commit_url,
          analise: analise,
        });

      } catch (error) {
        console.error(`‚ùå Erro durante a an√°lise ass√≠ncrona do commit: ${error.message}`);
        
        // Salvar erro no banco
        try {
          await saveAnalysis({
            commit_sha: commit_sha,
            commit_message: commit_message,
            author: author,
            repository: repository,
            timestamp: new Date().toISOString(),
            analysis_content: `Erro na an√°lise: ${error.message}`,
            status: 'Failed',
            commit_url: commit_url
          });
        } catch (saveError) {
          console.error(`‚ùå Erro ao salvar an√°lise com falha: ${saveError.message}`);
        }
      }
    })();

  } catch (error) {
    console.error("‚ùå Erro ao processar webhook:", error);
  }
});

